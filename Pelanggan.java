public class Pelanggan {
    private final String nama;
    private final String nomorHp;

    public Pelanggan(String nama, String nomorHp) {
        this.nama = nama;
        this.nomorHp = nomorHp;
    }

    public String getNama() {
        return nama;
    }

    public String getNomorHp() {
        return nomorHp;
    }
}
